#ifndef GPT_PWM_H
#define GPT_PWM_H

class PWM {
	const uint32_t GPT_MAX_PERCENT = 100;
	timer_ctrl_t * const _pTimerCtrl;
	timer_cfg_t const * const _pTimerCfg;
public:
    /*!------------------------------------------------
    @brief コンストラクタ
    -------------------------------------------------*/
    PWM(timer_ctrl_t * const pTimerCtrl, timer_cfg_t const * const pTimerCfg);

    /*!------------------------------------------------
    @brief デストラクタ
    -------------------------------------------------*/
    ~PWM() = default;
    
    /*!------------------------------------------------
	@brief 初期化
	-------------------------------------------------*/
	TYPE_ERROR Init(void);

	/*!------------------------------------------------
	@brief 出力開始
	-------------------------------------------------*/
	TYPE_ERROR Start(void);
	
	/*!------------------------------------------------
	@brief 出力停止
	-------------------------------------------------*/
	TYPE_ERROR Stop(void);
	
	/*!------------------------------------------------
	@brief リセット
	-------------------------------------------------*/
	TYPE_ERROR Reset(void);
	
	/*!------------------------------------------------
	@brief PWMデューティ比設定
	-------------------------------------------------*/
	TYPE_ERROR SetDuty(uint8_t Duty);
	
};
#endif

